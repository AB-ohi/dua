export const getDua1 = async () => {
    const url = "http://localhost:3000/dua?cat_id=1";
    try {
        const res = await fetch(url);
        const data = await res.json();
        return data;
    } catch (error) {
        console.error("Fetch error:", error);
        throw error;
    }
};
